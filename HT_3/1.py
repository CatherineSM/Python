#1. Створити цикл від 0 до ... (вводиться користувачем). В циклі створити 
#умову, яка буде виводити поточне значення, якщо остача від ділення на 17 
#дорівнює 0.

user_number = int(input('Веедите цело число, больше 0'))

list1 = []
for i in range(user_number):
    if i % 17 == 0:
        list1.append(i)

print(list1)